/*
  ? Operadores de Comparação, também conhecidos como Operdores Relacionais
  
  Os utilizamos quando queremos executar testes true/false e, em seguida agir de acordo, dependendo do resultado
  desse teste.

  ? ===  | Igualdade estritra
  Verifica se o valor da esquerda e o da direita são idênticos entre si.

  ? !==  | Não igualdade estrita
  Verifica se o valor da esquerda e o da direita não são idênticos entre si.

  ? <  |  Menor que
  Verifica se o valor da esquerda é menor que o valor da direita.

  ? >  | Maior que
  Verifica se o valor da esquerda é maior que o valor da direita

  ? <= | Menor ou igual que 
  Verifica se o valor da esquerda é menor que ou igual ao valor da direita.

  ? >= | Maior ou igual que 
  Veirifica se o valor da esquerda é maior que ou igual ao valor da direita.

  ! Nota: == e != são utilizados afim de testar igualdade ou desigualdade em JavaScrit.
  Estes são operadores válidos em JS, mas que diferem de  ===/!==. As versões anteriores testam se os valores são os
  mesmos, mas não se os tipos de dados dos valores são os mesmos. As últimas versões estritas testam a igualdade 
  de ambos os valores e seus tipos de dados. Eles tendem a resultar em menos erros, por isso é recomendado o uso da 
  versão estrita. 

  ! Operadores relacionais não tem ordem de precedência, são verificados da esquerda para a direita.

*/